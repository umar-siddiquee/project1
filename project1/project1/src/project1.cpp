#include<stdio.h>

int main()
{
	int day;
	printf("enter the number between 1-7 \n");
	scanf("%d",&day);
	switch(day)
	{
		case 1: printf("the day is mon");
		break;
		case 2: printf("the day is Tue.");
		break;
		case 3: printf("the day is Wed.");
		break;
		case 4: printf("the day is Thur.");
		break;
		case 5: printf("the day is Fri.");
		break;
		case 6: printf("the day is Sat");
		break;
		case 7: printf("the day is Sun");
		break;
		default: printf("You enter a wrong number");
	}
	return 0;
}
